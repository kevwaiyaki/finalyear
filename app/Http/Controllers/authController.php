     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $auth=new Auth;
        $auth->fname=$request->input('fname');
        $auth->email=$request->input('email');
        $auth->uname=$request->input('uname');
        $a=$request->input('psw');
        $b=$request->input('passco');
        if($a===$b){
            $enc=md5($a);
            $auth->psw=$enc;
            $auth->save();
        }
        return redirect()->route('Reply.create')
            ->with('success','Students created successfully.');
    }

